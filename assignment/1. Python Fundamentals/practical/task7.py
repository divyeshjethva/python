#  Functions and Methods

# Practical Example 1: Print "Hello" using a string
print("Hello")

# Practical Example 2: Allocate a string to a variable and print it
message = "Welcome to Python!"
print(message)

# Practical Example 3: Print a string using triple quotes
print("""This is printed using triple quotes.
It can span multiple lines.""")

# Practical Example 4: Access the first character of a string using index value
text = "Programming"
print("First character:", text[0])

# Practical Example 5: Access the string from the second position onwards using slicing
print("From second character onwards:", text[1:])

# Practical Example 6: Access a string up to the fifth character
print("Up to the fifth character:", text[:5])

# Practical Example 7: Print the substring between index values 1 and 4
print("Characters from index 1 to 4:", text[1:4])

# Practical Example 8: Print a string from the last character
print("Last character:", text[-1])

#Practical Example: 9) Write a Python program to print every alternate character from thestring starting from index 1
print("Every alternate character from index 1:", text[1::2])
